#include "libft.h"

int		ft_atoi(const char *str)
{
	long	result;
	long	sign;

	result = 0;
	while (ft_isspace(*str))
		str++;
	sign = (*str == '-') ? -1 : 1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		result = (result * 10) + (*str - '0');
		str++;
	}
	return ((int)(result * sign));
}
