#include "libft.h"

long		ft_sqrt(long n)
{
	long	sqrt;

	sqrt = 0;
	while ((sqrt * sqrt) != n && (sqrt * sqrt) < n)
		sqrt++;
	return (((sqrt * sqrt) == n) ? (sqrt) : (-1));
}
